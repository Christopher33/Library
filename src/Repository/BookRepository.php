    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Book::class);
    }

    public function getBooksByGenre()
    {

        // Traduire la requete en véritable requete SQL
        $queryBuilder = $this->createQueryBuilder('b');

        // Recuperer le query builder (car c'est le query builder qui premet de faire la requete SQL
        // Construire la requete facon SQL, mais en PHP
        $query = $queryBuilder->select('b')
            ->where('b.style = :style')
            ->setParameter('style', 'Roman')
            ->getQuery();

        // Exercuter la requete SQL en base de données pour recuperer les bons livres
        $books = $query->getArrayResult();

        return $books;

    }

